// Adds two numbers and returns the result
function add(a, b) {
    return a + b;
  }
  
  // Subtracts b from a and returns the result
  function subtract(a, b) {
    return a - b;
  }
  
  // Multiplies two numbers and returns the result
  function multiply(a, b) {
    return a * b;
  }
  
  // Divides a by b and returns the result
  function divide(a, b) {
    return a / b;
  }
  
  // Increments n and returns the result
  function increment(n) {
    return n + 1;
  }
  
  // Decrements n and returns the result
  function decrement(n) {
    return n - 1;
  }
  
  // Parses n as an integer and returns the parsed integer
  function makeInt(n) {
    return parseInt(n, 10); // Assumes base 10
  }
  
  // Parses n as a floating point number and returns the result
  function preserveDecimal(n) {
    return parseFloat(n);
  }
  